        <h5 class="card-title text-center">PENGERTIAN</h5>
<p class="card-text">
1. Pengaduan masyarakat adalah bentuk pengawasan masyarakat yang disampaikan
oleh masyarakat kepada Lembaga Ketahanan Nasional RI, berupa sumbangan
pemikiran, saran, gagasan atau keluhan/pengaduan yang bersifat membangun.</p>
 <p class="card-text">
2. Pengaduan masyarakat di Lingkugan Lembaga Ketahanan Nasional RI
dikelompokan dalam:
a. Pengaduan masyarakat berkadar pengawasan
b. Pengaduan masyarakat tidak berkadar pengawasan</p>
 <p class="card-text">
3. Pengaduan masyarakat berkadar pengawasan merupakan pengaduan masyarakat
yang isinya mengandung informasi atau adanya iindikasi terjadinya penyimpangan
atau penyalahgunaan wewenang yang dilakukan oleh aparatur Lembaga
Ketahanan Nasional RI sehingga mengakibatkan kerugian masyarakat atau negara.</p>
 <p class="card-text">
4. Pengaduan masyarakat tidak berkadar pengawasan merupakan pengaduan
masyarakat yang isinya mengandung informasi berupa sumbangan saran, kritik
yang kostruktif, dan lain sebagainya sehingga bermanfaat bagi perbaikan
penyelenggaraan pemerintahan dan pelayanan masyarakat.</p>
 <p class="card-text">
5. Pengawasan masyarakat adalah pengawasan yang dilakukan oleh masyarakat
terhadap penyelenggaraan pemerintahan.
6. Pimpinan instansi adalah pejabat pembina kepegawaian pada Lembaga Ketahanan
Nasional RI.</p>
 <p class="card-text">
7. Konfirmasi adalah proses kegiatan untuk mendapatkan penegasan mengenai
keberadaan terlapor yang teridentifikasi, baik bersifat perorangan, kelompok
maupun institusional, apabila memungkinkan termasuk masalah yang
dilaporkan/diadukan.</p>
 <p class="card-text">
8. Klarifikasi adalah proses penjernihan masalah atau kegiatan yang memberikan
penjelasan/data dokumen/bukti-bukti mengenai permasalahan yang diadukan pada
proporsi yang sebenarnya kepada sumber pengaduan dan instansi terkait.</p>
 <p class="card-text">
9. Audit adalah proses identifikasi masalah, analisis dan evaluasi bukti yang dilakukan
secara independen, obyektif, dan profesional berdasarkan standar yang berlaku,
untuk menilai kebenaran atas pengaduan masyarakat.</p>
 <p class="card-text">
10. Pelapor adalah individu atau kelompok masyarakat yang menyampaikan
pengaduan kepada Lembaga Ketahanan Nasional RI.</p>
 <p class="card-text">
11.Terlapor adalah aparatur negara atau kelompok masyarakat yang melakukan
penyimpangan atau pelanggaran.</p>
    

       
