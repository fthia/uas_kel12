        <h5 class="card-title text-center">PENCATATAN PENGADUAN</h5>
        <p class="card-text">
Pencatatan pengaduan masyarakat oleh Tim Dumasdu dilakukan sebagai berikut:</p>
 <p class="card-text">
1. Pengaduan masyarakat (dumas) yang diterima oleh Tim Dumasdu pada Unit Eselon
I berasal dari organisasi masyarakat, partai politik, perorangan atau penerusan
pengaduan ke Lemhannas RI dalam bentuk surat, fax, atau email, dicatat dalam
agenda surat masuk secara manual atau menggunakan aplikasi sesuai dengan
prosedur pengadministrasian/tata persuratan yang berlaku. Pengaduan yang
disampaikan secara lisan agar dituangkan ke dalam formulir yang disediakan.</p>
 <p class="card-text">
2. Pencatatan dumas tersebut sekurang-kurangnya memuat informasi tentang Nomor
dan tanggal surat pengaduan, tanggal diterima, identitas pengadu, identitas terlapor,
dan inti pengaduan.</p>
 <p class="card-text">
3. Pengaduan yang alamatnya jelas, segera dijawab secara tertulis dalam waktu
paling lambat 14 (empat belas) hari kerja sejak surat pengaduan diterima, dengan
tembusan disampaikan kepada Sekretariat Tim Dumasdu pada Inspektorat Lembaga
Ketahanan Nasional RI.
</p>
