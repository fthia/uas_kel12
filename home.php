    <h5 class="card-title text-center">PENANGANAN PANDUAN MASYARAKAT</h5>
    <p class="card-text">

Pengaduan masyarakat yang diterima Lemhannas RI ditangani oleh Tim Penanganan
Pengaduan Masyarakat Terpadu di lingkungan Lembaga Ketahanan Nasional RI (Tim
Dumasdu) yang ada pada masing-masing Unit Eselon I.</p>
<p class="card-text">
Pengaduan masyarakat oleh Tim Dumasdu dilakukan berdasarkan kewenangan dan
kriteria, bahwa pengaduan berindikasi penyimpangan yang merugikan
masyarakat/negara ditangani oleh Tim Dumasdu pada Inspektorat Lembaga Ketahanan
Nasional RI, sedangkan indikasi pengaduan di luar itu maupun yang berupa sumbang
saran, kritik yang konstruktif, yang bermanfaat bagi perbaikan penyelenggaraan
pemerintahan dan pelayanan masyarakat menjadi fokus penanganan oleh Tim
Dumasdu pada unit eselon I yang lain.</p>
<p class="card-text">
Pengaduan yang jelas alamatnya, segera
dijawab secara tertulis dalam waktu paling lambat 14 (empat belas) hari kerja sejak
pengaduan diterima, dan diselesaikan dala waktu paling lambat 90 (sembilan puluh) hari
kerja sejak pengaduan tersebut diterima oleh Lembaga Ketahanan Nasional RI
Penanganan pengaduan masyarakat meliputi kegiatan penerimaan, pencatatan,
penelaahan, penyaluran, konfirmasi, klarifikasi atau penelitian, pemeriksaan,
pelaporan, tindak lanjut, dan pengarsipan.</p>
